// If else in C -- 5

# include <stdio.h>
int main(){

   int i = 20;
   if (i == 10)
       printf("\n i is 10");
    else if (i == 15)
        printf("\n i is 15");
    else if (i == 20)
        printf("\n i is 20");
    else
    {
        printf("\n i is not present");
    }
    
    return 0;
}