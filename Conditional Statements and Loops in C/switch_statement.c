// Switch Statement in C

# include <stdio.h>
int main(){

    int x = 3;

    switch (x)
    {
    case 1:
        printf("\n Choice is 1");
        break;
    case 2:
        printf("\n Choice is 2");
        break;
    case 3:
        printf("\n Choice is 3");
        break;
    case 4:
        printf("\n Choice is 4");
        break;
    case 5:
        printf("\n Choice is 5");
        break;
    default:
        printf("\n Choice is other than 1, 2, 3, 4, 5");
        break;
    }

    return 0;

}