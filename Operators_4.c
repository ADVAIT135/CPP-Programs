// Operators in C -- 4

# include <stdio.h>

int main(){

    int x= 12, y = 13;
    printf("\n X= %d, Y= %d", x, y);

    printf("\n x>y : %d", x>y); // Greater than
    printf("\n x<y : %d", x<y); // Less than
    printf("\n x>=y : %d", x>=y); // Greater than or equal to
    printf("\n x<=y : %d", x<=y); // Less than or equal to
    printf("\n x==y : %d", x==y); // Equal to
    printf("\n x!=y : %d", x!=y); // Not equal to

    return 0;
}